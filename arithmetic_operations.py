print(10 + 3)  # Addition
print(10 - 3)  # Subtraction
print(10 * 3)  # Multiplication
print(10 / 3)  # Division (float)
print(10 // 3)  # Division (drops the floating point)
print(10 % 3)  # Modulo (shows the remainder of the division)
print(10 ** 3)  # Exponent (the power)

x = 10
x = x + 3  # increments number
print(x)

x += 3  # Augmented assignment operator subtract works too.

